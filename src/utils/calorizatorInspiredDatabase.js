// Функция поиска продукта в базе
export const findProductInDatabase = (productName) => {
  const normalizedName = productName.toLowerCase().trim();
  
  // Поиск по всем категориям
  for (const category in calorizatorInspiredDatabase) {
    const products = calorizatorInspiredDatabase[category];
    for (const product in products) {
      if (product.toLowerCase().includes(normalizedName) || 
          normalizedName.includes(product.toLowerCase())) {
        return {
          ...products[product],
          name: product,
          category: category
        };
      }
    }
  }
  
  return null;
};

